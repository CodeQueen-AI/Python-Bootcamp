# DataTypes
age = 25 #positive int
temperature = -20 #negative int
count = 0 #zero
print(age , temperature , count)

#Float
pi = 3.14
print(pi)

#complex
z1 = 3 + 4j
print(z1)

#str (string)
name = 'codequeen'
print(name)

#list
fruits = ['apple' , 'orange' , 'mango']
print(fruits)

#Tuple
numbers = (10 , 30)
print(numbers)

#Set
unique_numbers = (1 ,2 ,3, 4)
print(unique_numbers)


#frozenset
vowels = frozenset("aeiou")  
